export function parseDate(ddMonYy) {
  const [dd, mon, yy] = ddMonYy.split('-')
  const months = { Jan: '01', Feb: '02', Mar: '03', Apr: '04', May: '05', Jun: '06',
    Jul: '07', Aug: '08', Sep: '09', Oct: '10', Nov: '11', Dec: '12' }
  return `20${yy}-${months[mon]}-${dd.padStart(2, '0')}`
}

export function localKickoffToIso(dateStr, timeStr, city) {
  const offset = CITY_UTC_OFFSET[city]
  if (!offset) throw new Error(`Unknown city timezone: ${city}`)
  const [h, m] = timeStr.split(':')
  return new Date(`${dateStr}T${h.padStart(2, '0')}:${m}:00${offset}`).toISOString()
}
